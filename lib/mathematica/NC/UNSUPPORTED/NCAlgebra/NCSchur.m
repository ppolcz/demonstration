(*    This defines a function with some of the properties of the      *)
(*    Schur product.                                                  *)
(* ------------------------------------------------------------------ *)
SetNonCommutative[Schur];
SetBilinear[Schur];

Schur[a_,Schur[b_,c_]]:=Schur[Schur[a,b],c];
