% --- Executes on button press in draw_btn.
function draw_btn_Callback(hObject, eventdata, handles)
% hObject    handle to draw_btn (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
clear x y
hold off



axes(handles.axes1)
plot(-9999,-9999)

xmin = str2num(get(handles.xmin,'string'));
xmax = str2num(get(handles.xmax,'string'));
ymin = str2num(get(handles.ymin,'string'));
ymax = str2num(get(handles.ymax,'string'));
axis([xmin xmax ymin ymax])
n = 1;
hold on
while(1)
    [a, b] = ginput(1);
    
    if(n==1)
        x(1) = xmin;
        y(1) = b;
        n = n+1;
    end
    
    if(n~=1)
        if(a<x(n-1))
         a = x(n-1);
        end
    end
    if(~(a>=xmin & a<=xmax & b>=ymin & b<=ymax))
        x(n) = xmax;
        y(n) = y(n-1);
        
        plot([x(n-1) xmax],[y(n-1) y(n-1)],'k','Linewidth',2)
        axis([xmin xmax ymin ymax])
        break
    end
    
    x(n) = a;
    y(n) = b;
    if(n==1)
        plot([xmin a],[b b],'k','Linewidth',2)
    else
        plot([x(n-1) x(n)],[y(n-1) y(n)],'k','Linewidth',2)
    end
    
    n = n+1;
    axis([xmin xmax ymin ymax])
end
hold off

plot(x,y,'k','Linewidth',2)
axis([xmin xmax ymin ymax])
